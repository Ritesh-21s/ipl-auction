export function findNationality(playerName, playerTeamName) {

  const teamKey = playerTeamName.toUpperCase();

  const players = cricPlayers[teamKey];
  const nationalities = nationalityList[teamKey];

  if (!players || !nationalities) {
    console.warn("Invalid team:", playerTeamName);
    return "i"; // default safe value
  }

  // normalize strings (IMPORTANT)
  const normalizedPlayer = playerName.replace(/\s+/g, " ").trim();

  const playerIndex = players.findIndex(
    p => p.replace(/\s+/g, " ").trim() === normalizedPlayer
  );

  if (playerIndex === -1) {
    console.warn(`Player not found: ${playerName} in ${teamKey}`);
    return "i"; // default instead of crashing
  }

  return nationalities[playerIndex] ?? "i";
}
